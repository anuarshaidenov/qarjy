const useScrollTransform = (
  startX: number,
  endX: number,
  startY: number,
  endY: number
) => {
  const { scrollYProgress } = useScrollYProgress();

  const x = useTransform(scrollYProgress, [0, 0.5], [startX, endX]);
  const y = useTransform(scrollYProgress, [0, 0.5], [startY, endY]);

  const transform = useMotionTemplate`translateX(${x}px) translateY(${y}px)`;

  return transform;
};
